<?php

    /* ********************************************************
     * ********************************************************
     * ********************************************************/
    class DoFactory {
        
        /* ********************************************************
		 * ********************************************************
		 * ********************************************************/
		public function get(string $class_actor, $attributes = null) {
			$class_name = $class_actor . "Do";
			
			return new $class_name($attributes, strtolower($class_actor));
		}

    }

?>